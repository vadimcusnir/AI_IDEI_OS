import { useState } from "react";
import { Download, FileText, FileJson, File } from "lucide-react";
import {
  DropdownMenu, DropdownMenuContent, DropdownMenuItem, DropdownMenuTrigger, DropdownMenuSeparator,
} from "@/components/ui/dropdown-menu";
import { Button } from "@/components/ui/button";
import { toast } from "sonner";

interface ArtifactExportMenuProps {
  title: string;
  content: string;
  metadata?: Record<string, unknown>;
  tags?: string[];
  artifactType?: string;
  serviceKey?: string | null;
  createdAt?: string;
}

function downloadFile(filename: string, content: string, mimeType: string) {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}

function slugify(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 60);
}

export function ArtifactExportMenu({
  title, content, metadata, tags, artifactType, serviceKey, createdAt,
}: ArtifactExportMenuProps) {
  const [exporting, setExporting] = useState(false);
  const slug = slugify(title || "artifact");

  const exportMarkdown = () => {
    const header = `# ${title}\n\n> Generated by AI-IDEI${serviceKey ? ` — ${serviceKey}` : ""}\n> ${createdAt ? new Date(createdAt).toLocaleDateString("ro-RO") : ""}\n\n---\n\n`;
    downloadFile(`${slug}.md`, header + content, "text/markdown;charset=utf-8");
    toast.success("Exported as Markdown");
  };

  const exportPlainText = () => {
    const header = `${title}\n${"=".repeat(title.length)}\n\nGenerated by AI-IDEI${serviceKey ? ` | Service: ${serviceKey}` : ""}\n${createdAt ? `Date: ${new Date(createdAt).toLocaleDateString("ro-RO")}` : ""}\n\n---\n\n`;
    // Strip markdown formatting for plain text
    const plainContent = content
      .replace(/#{1,6}\s/g, "")
      .replace(/\*\*(.*?)\*\*/g, "$1")
      .replace(/\*(.*?)\*/g, "$1")
      .replace(/`(.*?)`/g, "$1")
      .replace(/>\s/g, "  ");
    downloadFile(`${slug}.txt`, header + plainContent, "text/plain;charset=utf-8");
    toast.success("Exported as TXT");
  };

  const exportJSON = () => {
    const data = {
      title,
      content,
      artifact_type: artifactType || "document",
      service_key: serviceKey || null,
      tags: tags || [],
      metadata: metadata || {},
      exported_at: new Date().toISOString(),
      created_at: createdAt || null,
      platform: "AI-IDEI",
    };
    downloadFile(`${slug}.json`, JSON.stringify(data, null, 2), "application/json;charset=utf-8");
    toast.success("Exported as JSON");
  };

  const copyToClipboard = async () => {
    try {
      await navigator.clipboard.writeText(content);
      toast.success("Content copied to clipboard");
    } catch {
      toast.error("Failed to copy");
    }
  };

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button variant="outline" size="sm" className="gap-1.5 text-xs h-7">
          <Download className="h-3 w-3" />
          Export
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end" className="w-44">
        <DropdownMenuItem onClick={exportMarkdown} className="gap-2 text-xs">
          <FileText className="h-3.5 w-3.5" />
          Markdown (.md)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={exportPlainText} className="gap-2 text-xs">
          <File className="h-3.5 w-3.5" />
          Plain Text (.txt)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={exportJSON} className="gap-2 text-xs">
          <FileJson className="h-3.5 w-3.5" />
          JSON (.json)
        </DropdownMenuItem>
        <DropdownMenuSeparator />
        <DropdownMenuItem onClick={copyToClipboard} className="gap-2 text-xs">
          <FileText className="h-3.5 w-3.5" />
          Copy to Clipboard
        </DropdownMenuItem>
      </DropdownMenuContent>
    </DropdownMenu>
  );
}

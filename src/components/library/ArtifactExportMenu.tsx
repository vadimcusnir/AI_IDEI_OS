import { useState } from "react";
import { Download, FileText, FileJson, File, FileType, Copy, Archive, Printer } from "lucide-react";
import {
  DropdownMenu, DropdownMenuContent, DropdownMenuItem, DropdownMenuTrigger, DropdownMenuSeparator,
} from "@/components/ui/dropdown-menu";
import { Button } from "@/components/ui/button";
import { toast } from "sonner";
import { useTranslation } from "react-i18next";

interface ArtifactExportMenuProps {
  title: string;
  content: string;
  metadata?: Record<string, unknown>;
  tags?: string[];
  artifactType?: string;
  serviceKey?: string | null;
  createdAt?: string;
}

function downloadFile(filename: string, content: string | Blob, mimeType: string) {
  const blob = content instanceof Blob ? content : new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}

function slugify(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 60);
}

function generateHTMLDoc(title: string, content: string, serviceKey?: string | null, createdAt?: string): string {
  // SECURITY: Import synchronously since this is called from event handlers
  const escapeHtml = (str: string) => str
    .replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;").replace(/'/g, "&#39;");

  const safeTitle = escapeHtml(title);
  const safeServiceKey = serviceKey ? escapeHtml(serviceKey) : null;

  // Escape content first, then apply markdown-like formatting
  let html = escapeHtml(content)
    .replace(/^### (.*$)/gm, "<h3>$1</h3>")
    .replace(/^## (.*$)/gm, "<h2>$1</h2>")
    .replace(/^# (.*$)/gm, "<h1>$1</h1>")
    .replace(/\*\*(.*?)\*\*/g, "<strong>$1</strong>")
    .replace(/\*(.*?)\*/g, "<em>$1</em>")
    .replace(/`(.*?)`/g, "<code>$1</code>")
    .replace(/^- (.*$)/gm, "<li>$1</li>")
    .replace(/^&gt; (.*$)/gm, "<blockquote>$1</blockquote>")
    .replace(/\n\n/g, "</p><p>")
    .replace(/\n/g, "<br/>");

  return `<!DOCTYPE html>
<html><head>
<meta charset="utf-8">
<title>${safeTitle}</title>
<style>
  body { font-family: 'Segoe UI', Calibri, Arial, sans-serif; max-width: 800px; margin: 40px auto; padding: 0 20px; line-height: 1.6; color: #1a1a1a; }
  h1 { font-size: 28px; border-bottom: 2px solid #e5e5e5; padding-bottom: 8px; }
  h2 { font-size: 22px; margin-top: 24px; }
  h3 { font-size: 18px; margin-top: 20px; }
  blockquote { border-left: 3px solid #ccc; margin: 12px 0; padding: 8px 16px; color: #555; }
  code { background: #f4f4f4; padding: 2px 6px; border-radius: 3px; font-size: 14px; }
  .meta { color: #888; font-size: 12px; margin-bottom: 24px; }
</style>
</head><body>
<h1>${safeTitle}</h1>
<div class="meta">Generated by AI-IDEI${safeServiceKey ? ` — ${safeServiceKey}` : ""}${createdAt ? ` | ${new Date(createdAt).toLocaleDateString()}` : ""}</div>
<p>${html}</p>
</body></html>`;
}

export function ArtifactExportMenu({
  title, content, metadata, tags, artifactType, serviceKey, createdAt,
}: ArtifactExportMenuProps) {
  const { t } = useTranslation(["common", "errors"]);
  const slug = slugify(title || "artifact");

  const exportMarkdown = () => {
    const header = `# ${title}\n\n> Generated by AI-IDEI${serviceKey ? ` — ${serviceKey}` : ""}\n> ${createdAt ? new Date(createdAt).toLocaleDateString() : ""}\n\n---\n\n`;
    downloadFile(`${slug}.md`, header + content, "text/markdown;charset=utf-8");
    toast.success(t("common:exported_as", { format: "Markdown" }));
  };

  const exportPlainText = () => {
    const header = `${title}\n${"=".repeat(title.length)}\n\nGenerated by AI-IDEI${serviceKey ? ` | Service: ${serviceKey}` : ""}\n${createdAt ? `Date: ${new Date(createdAt).toLocaleDateString()}` : ""}\n\n---\n\n`;
    const plainContent = content
      .replace(/#{1,6}\s/g, "")
      .replace(/\*\*(.*?)\*\*/g, "$1")
      .replace(/\*(.*?)\*/g, "$1")
      .replace(/`(.*?)`/g, "$1")
      .replace(/>\s/g, "  ");
    downloadFile(`${slug}.txt`, header + plainContent, "text/plain;charset=utf-8");
    toast.success(t("common:exported_as", { format: "TXT" }));
  };

  const exportJSON = () => {
    const data = {
      title,
      content,
      artifact_type: artifactType || "document",
      service_key: serviceKey || null,
      tags: tags || [],
      metadata: metadata || {},
      exported_at: new Date().toISOString(),
      created_at: createdAt || null,
      platform: "AI-IDEI",
    };
    downloadFile(`${slug}.json`, JSON.stringify(data, null, 2), "application/json;charset=utf-8");
    toast.success(t("common:exported_as", { format: "JSON" }));
  };

  const exportHTML = () => {
    const html = generateHTMLDoc(title, content, serviceKey, createdAt);
    downloadFile(`${slug}.html`, html, "text/html;charset=utf-8");
    toast.success(t("common:exported_as", { format: "HTML" }));
  };

  const exportPDF = () => {
    const html = generateHTMLDoc(title, content, serviceKey, createdAt);
    const printWindow = window.open("", "_blank");
    if (!printWindow) {
      toast.error("Pop-up blocked. Please allow pop-ups.");
      return;
    }
    printWindow.document.write(html);
    printWindow.document.close();
    printWindow.onload = () => {
      printWindow.print();
    };
    toast.success(t("common:exported_as", { format: "PDF" }));
  };

  const exportCSV = () => {
    const lines = content.split("\n").filter(l => l.trim());
    const csvRows = ["\"Section\",\"Content\""];
    let currentSection = "Introduction";
    for (const line of lines) {
      const headingMatch = line.match(/^#{1,3}\s+(.+)/);
      if (headingMatch) {
        currentSection = headingMatch[1];
      } else if (line.trim() && !line.startsWith("---")) {
        const clean = line.replace(/\*\*/g, "").replace(/\*/g, "").replace(/`/g, "").replace(/^[->]\s*/, "");
        csvRows.push(`"${currentSection.replace(/"/g, '""')}","${clean.replace(/"/g, '""')}"`);
      }
    }
    downloadFile(`${slug}.csv`, csvRows.join("\n"), "text/csv;charset=utf-8");
    toast.success(t("common:exported_as", { format: "CSV" }));
  };

  const copyToClipboard = async () => {
    try {
      await navigator.clipboard.writeText(content);
      toast.success(t("common:copied_to_clipboard"));
    } catch {
      toast.error(t("errors:copy_failed"));
    }
  };

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button variant="outline" size="sm" className="gap-1.5 text-xs h-7">
          <Download className="h-3 w-3" />
          {t("common:export")}
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end" className="w-52">
        <DropdownMenuItem onClick={exportMarkdown} className="gap-2 text-xs">
          <FileText className="h-3.5 w-3.5" />
          Markdown (.md)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={exportPlainText} className="gap-2 text-xs">
          <File className="h-3.5 w-3.5" />
          Plain Text (.txt)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={exportJSON} className="gap-2 text-xs">
          <FileJson className="h-3.5 w-3.5" />
          JSON (.json)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={exportHTML} className="gap-2 text-xs">
          <FileType className="h-3.5 w-3.5" />
          HTML (.html)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={exportPDF} className="gap-2 text-xs">
          <Printer className="h-3.5 w-3.5" />
          PDF (Print)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={exportCSV} className="gap-2 text-xs">
          <Archive className="h-3.5 w-3.5" />
          CSV (.csv)
        </DropdownMenuItem>
        <DropdownMenuSeparator />
        <DropdownMenuItem onClick={copyToClipboard} className="gap-2 text-xs">
          <Copy className="h-3.5 w-3.5" />
          {t("common:copy_clipboard")}
        </DropdownMenuItem>
      </DropdownMenuContent>
    </DropdownMenu>
  );
}